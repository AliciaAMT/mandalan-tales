<td class="left"><br />
<p>The shimmering of a small light and the fast and deep shadows conceal most of the small room. Thick smoke tickles your nose and eyes as you slowly wake up in a strange bed with bedding that is course and rough. Slowly you try to sit up, yet all your bones hurt, and the smallest movement makes your poor head spin. A cool hand touches your forehead and pushes you softly back in the pillows. Through slits in your eyes you see candle-light as you hear a thunderstorm outside. The shutters in front of the small window above you clatter.</p>
 <p>
As you try to speak your voice brakes in rasps and ends in a loud cough.</p><p> 
" Here, drink some" a soft voice says.</p><p> 
You feel a rough wooden cup on your lips and a moment later fresh cold water seeps into your parched mouth. </p><p>

A moment later your eyes finally open enough to look around the small room, wich is made from rough wooden planks, and heavy beams hold up a roof made of reeds. On the wall you see a plank that is filled with books and a cantarn with fresh flowers. Just now you notice the smell, a smell that reminds you of  home, yet you know this not your home. </p><p>

The bed creakes as you sit up, and the small amount of water is helping you not to empty your stomache. Candles are placed on a small desk at the far end of the small room, and your cloths are hanging over a simple chair. On another end of the room you see a large water barrel and an old-fashioned wash-basin. Why would someone have these things around in a time of running water and faucets?</p><p>
 
Lightning flashes and lights more of the room up, and only then do you see the man beside you.</p>

<p>As soon as you sit up and your head stops spinning, and the cool water takes away the worst of the thirst. The man next to you starts talking.</p>
<p>"How are you feeling?" he says in a soft and friendly voice. After licking your lips and your eyes focused on the man you see him smile, and he hands you the cup of water again. Once more the storm outside attracts your attention. "Do you remember the spring storms?" the man asks.</p>
<p>You can only shake your head. Spring at home never has storms like this you think as a bolt lightens up the room and the water basin. "What happened to having a sink?" you ask with voice that sounds like sandpaper.</p> 
<p>"Sink?" the man asks. "You must still be half knocked out, you will feel better soon."</p> 
<p>"Were am I?" you ask.</p> 
<p>"Home my child."</p> 
<p>"Your child?!" you exclaim. In the commotion you drop the cup on the wooden floor, and the fresh water seeps through the slits of wood and soon disappears.</p> 
<p>
"Fret not my child, I understand you are confused. As well am I... you are my child, yet you are different somehow. Solias will know what to do."</p> 
<p>"Who?"</p>
<p>"He is an Elder in the Counsel, and he is a wise man. He will be able to help you."</p> 
<p>"What happened to the airplane? Was there a big crash...oh no...all those people...." Your voice trails off.</p> 
<p>"A what?" the man asks with a look of amazement on his face.</p> 
<p>"Airplane...you know? Big metal object that flies?" you say with a tone of annoyance in your voice. How can someone not know what an airplane is?</p> 
<p>"Metal does not fly, son, unless someone throws it. Well, you can also make a dwarf mad, and yes, metal does fly," the man laughs heartily.</p>
<p>"Dwarves? Are we talking about midgets? Small people?" you ask confusedly.</p> 
<p>"Yes they are smaller, yet fierce and powerful warriors. Stand not between them and their ale!" The man laughs and you can not help but smile. All this talk of dwarves and Elders is making your head spin again. Where are you?</p>
<p>"Do you mean that you can fly on any piece of metal?" the man asks without looking at you. You see he is embarassed to ask... and almost afraid as well.</p> 
<p>"It's like a metal bird that doesn't flap it's wings, it just flies. That is how I got here," you explain wearily.</p> 
<p>"Hmmm." The man gets up and walks over to the water basin and splashes his face with water. The shutters clatter under another crack of thunder. "You must be a very powerful mystic indeed..." He turns to you abruptly and says, "You need to see Solias as soon as you feel well my child. There is not a lot of time. Sleep now."</p>
<p>With those words he leaves the room, and soon you feel tired and sleep grasps you firmly as you drift off in a dreamless night.</p>
<p>
The morning greets you with a warm sun that peeks through the cracks of the shutters, and you lift your arm and flip them open. A smell enters the small room like you have never smelled before. You sit up on your knees and look outside. Green hills and farmlands surround the small house. People are working and talking, and a warm breeze carries the smell of clean air.</p>
<p>
"Ah you're up, and I brought breakfast," the man says as he hands you a wooden plate with meat and cheese.</p>
<p>"Where am I?" you ask between bites.</p> 
<p>"Home....at last" the man smiles and there is love in his eyes.</p> 
<p>Breakfast is gone with a few big bites, and you feel a lot better. There is strength in your limbs again and the fresh air cleared your head. Despite your growing confusion, somehow this all feels...right.</p>
<p>"Here, for you," says the man as he hands you a leather bag.</p> 
<p>"Thank you, but I can't..." The bag looks expensive and old. This leatherwork is not cheap.</p> 
<p>"Do not be daft! It is your birthday present. It has served me well over the many years, and I am sure you will need it. It holds everything that you would ever want to carry."</p> 
<p>You open the bag and see that is very supple, and you smell the oils in the leather.</p>
<p>"Thank you," you reply, wondering why you will need this bag...</p> 
<p>"That is not all, this is also yours." He hands you a leatherbound book with symbols on the cover. "Solias will help you with this."</p> 
<p>As you flip through the pages you see that they contain a few incantations that somehow look familiar.</p>
<p>"I'll give you some time to get reaquainted with your spell book, and then I have something else for you when you are ready..." says your new "father".</p>
